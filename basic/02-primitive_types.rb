### Primitive types and variables

## Integer numbers

puts "Example of #{10.class} class"
puts 5
puts 10

## Real numbers

puts "Example of #{10.1.class} class"
puts 5.1
puts 10.2

## Chars and Strings same class 

puts "Example of #{"just one string".class} class"
puts "Hello World"
puts "@"
puts "A"

## Bolean

puts "Example of booleans #{true.class} class or even #{false.class} class"

puts true
puts false