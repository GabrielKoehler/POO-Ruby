# Operators available

# Bigger >
puts 2 > 1

# Less <
puts 1 < 4

# Bigger or equal >=
puts 3 >= 2

# Lesser or equal <=
puts 2 <= 3

# Equal ==
puts 2 == 2

# Not equal !=
puts 3 != 6

# Spaceship <=> have 3 results
# if the right side value is bigger than left side, returns -1
# if the right side value is equal than left side, returns 0
# if the right side value is lesser than left side, returns 1
puts 1 <=> 3
puts 1 <=> 1
puts 3 <=> 1